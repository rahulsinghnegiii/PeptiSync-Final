import Navigation from "@/components/Navigation";
import Footer from "@/components/Footer";
import { motion } from "framer-motion";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Tabs, TabsContent, TabsList, TabsTrigger } from "@/components/ui/tabs";
import { Label } from "@/components/ui/label";
import { useState, useEffect } from "react";
import { toast } from "sonner";
import { Mail, Lock, User, Check, X } from "lucide-react";
import { useAuth } from "@/contexts/AuthContext";
import { useNavigate, Link } from "react-router-dom";
import { auth, db } from "@/lib/firebase";
import { collection, addDoc, Timestamp } from "firebase/firestore";
import { COLLECTIONS } from "@/lib/firestoreHelpers";
import { validatePasswordStrength, getPasswordStrengthLabel, getPasswordStrengthColor } from "@/lib/passwordValidation";

const Auth = () => {
  const [isLoading, setIsLoading] = useState(false);
  const [loginData, setLoginData] = useState({ email: "", password: "" });
  const [signupData, setSignupData] = useState({ name: "", email: "", password: "", confirmPassword: "" });
  const { signIn, signUp, signInWithGoogle, user } = useAuth();
  const navigate = useNavigate();

  useEffect(() => {
    // Redirect authenticated users to dashboard
    if (user) {
      navigate("/dashboard");
    }
  }, [user, navigate]);

  const handleLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    setIsLoading(true);
    
    try {
      const { error } = await signIn(loginData.email, loginData.password);
      
      if (error) {
        // Provide user-friendly error messages
        let errorMessage = "Failed to sign in";
        if (error.code === "auth/invalid-credential" || error.code === "auth/user-not-found") {
          errorMessage = "Invalid email or password. Please check your credentials or sign up for a new account.";
        } else if (error.code === "auth/wrong-password") {
          errorMessage = "Incorrect password. Please try again.";
        } else if (error.code === "auth/too-many-requests") {
          errorMessage = "Too many failed login attempts. Please try again later.";
        } else if (error.code === "auth/user-disabled") {
          errorMessage = "This account has been disabled.";
        } else {
          errorMessage = error.message || errorMessage;
        }
        toast.error(errorMessage);
      } else {
        toast.success("Welcome back! You've been logged in successfully.");
        
        // Check for pending cart item
        const pendingItem = localStorage.getItem('pendingCartItem');
        if (pendingItem) {
          try {
            const product = JSON.parse(pendingItem);
            const currentUser = auth.currentUser;
            
            if (currentUser) {
              await addDoc(collection(db, COLLECTIONS.CART_ITEMS), {
                userId: currentUser.uid,
                productName: product.name,
                productPrice: parseFloat(product.price.replace('$', '')),
                productImage: product.image,
                quantity: 1,
                createdAt: Timestamp.now(),
                updatedAt: Timestamp.now(),
              });
              toast.success("Product added to cart!");
            }
            localStorage.removeItem('pendingCartItem');
          } catch (err) {
            console.error("Error adding pending cart item:", err);
          }
        }
        
        navigate("/dashboard");
      }
    } catch (error) {
      console.error("Sign in error:", error);
      toast.error("An unexpected error occurred");
    } finally {
      setIsLoading(false);
    }
  };

  const handleSignup = async (e: React.FormEvent) => {
    e.preventDefault();
    
    if (signupData.password !== signupData.confirmPassword) {
      toast.error("Passwords don't match");
      return;
    }

    // Validate password strength
    const passwordStrength = validatePasswordStrength(signupData.password);
    if (!passwordStrength.isValid) {
      toast.error("Password does not meet requirements");
      return;
    }
    
    setIsLoading(true);
    
    try {
      const { error } = await signUp(signupData.email, signupData.password, signupData.name);
      
      if (error) {
        // Provide user-friendly error messages
        let errorMessage = "Failed to create account";
        if (error.code === "auth/email-already-in-use") {
          errorMessage = "This email is already registered. Please sign in instead.";
        } else if (error.code === "auth/invalid-email") {
          errorMessage = "Invalid email address format.";
        } else if (error.code === "auth/weak-password") {
          errorMessage = "Password is too weak. Please use a stronger password.";
        } else {
          errorMessage = error.message || errorMessage;
        }
        toast.error(errorMessage);
      } else {
        toast.success("Account created successfully! Welcome to PeptiSync.");
        
        // Check for pending cart item
        const pendingItem = localStorage.getItem('pendingCartItem');
        if (pendingItem) {
          try {
            const product = JSON.parse(pendingItem);
            const currentUser = auth.currentUser;
            
            if (currentUser) {
              await addDoc(collection(db, COLLECTIONS.CART_ITEMS), {
                userId: currentUser.uid,
                productName: product.name,
                productPrice: parseFloat(product.price.replace('$', '')),
                productImage: product.image,
                quantity: 1,
                createdAt: Timestamp.now(),
                updatedAt: Timestamp.now(),
              });
              toast.success("Product added to cart!");
            }
            localStorage.removeItem('pendingCartItem');
          } catch (err) {
            console.error("Error adding pending cart item:", err);
          }
        }
        
        navigate("/dashboard");
      }
    } catch (error) {
      console.error("Sign up error:", error);
      toast.error("An unexpected error occurred");
    } finally {
      setIsLoading(false); 
    }
  };

  const handleGoogleSignIn = async () => {
    setIsLoading(true);
    
    try {
      const { error } = await signInWithGoogle();
      
      if (error) {
        let errorMessage = "Failed to sign in with Google";
        if (error.code === "auth/popup-closed-by-user") {
          errorMessage = "Sign-in cancelled";
        } else if (error.code === "auth/popup-blocked") {
          errorMessage = "Popup blocked. Please allow popups for this site.";
        } else if (error.code === "auth/account-exists-with-different-credential") {
          errorMessage = "An account already exists with this email using a different sign-in method.";
        } else if (error.code === "auth/cancelled-popup-request") {
          errorMessage = "Sign-in cancelled";
        } else {
          errorMessage = error.message || errorMessage;
        }
        toast.error(errorMessage);
      } else {
        toast.success("Welcome! You've been signed in successfully.");
        
        // Handle pending cart item (same as email/password)
        const pendingItem = localStorage.getItem('pendingCartItem');
        if (pendingItem) {
          try {
            const product = JSON.parse(pendingItem);
            const currentUser = auth.currentUser;
            
            if (currentUser) {
              await addDoc(collection(db, COLLECTIONS.CART_ITEMS), {
                userId: currentUser.uid,
                productName: product.name,
                productPrice: parseFloat(product.price.replace('$', '')),
                productImage: product.image,
                quantity: 1,
                createdAt: Timestamp.now(),
                updatedAt: Timestamp.now(),
              });
              toast.success("Product added to cart!");
            }
            localStorage.removeItem('pendingCartItem');
          } catch (err) {
            console.error("Error adding pending cart item:", err);
          }
        }
        
        navigate("/dashboard");
      }
    } catch (error) {
      console.error("Google sign-in error:", error);
      toast.error("An unexpected error occurred");
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="min-h-screen">
      <Navigation />
      <main className="pt-20">
        {/* Background Effects */}
        <div className="fixed inset-0">
          {Array.from({ length: 20 }).map((_, i) => (
            <motion.div
              key={i}
              className="absolute w-1 h-1 bg-primary rounded-full"
              initial={{ 
                x: Math.random() * (typeof window !== 'undefined' ? window.innerWidth : 1200),
                y: Math.random() * (typeof window !== 'undefined' ? window.innerHeight : 800),
                opacity: 0 
              }}
              animate={{
                y: [null, -100, -200],
                opacity: [0, 0.6, 0],
              }}
              transition={{
                duration: 3 + Math.random() * 2,
                repeat: Infinity,
                delay: Math.random() * 2,
              }}
            />
          ))}
        </div>

        <section className="relative py-20 flex items-center justify-center min-h-[calc(100vh-80px)]">
          <div className="max-w-md w-full mx-auto px-4">
            <motion.div
              initial={{ y: 50, opacity: 0 }}
              animate={{ y: 0, opacity: 1 }}
              transition={{ duration: 0.8 }}
            >
              <Card className="glass border-glass-border shadow-2xl">
                <CardHeader className="text-center pb-6">
                  <div className="flex items-center justify-center mb-4">
                    <img 
                      src="/logo.png" 
                      alt="PeptiSync Logo" 
                      className="w-12 h-12 object-contain"
                    />
                  </div>
                  <CardTitle className="text-2xl font-bold text-gradient">
                    Welcome to PeptiSync
                  </CardTitle>
                </CardHeader>
                
                <CardContent>
                  <Tabs defaultValue="login" className="w-full">
                    <TabsList className="glass w-full mb-6">
                      <TabsTrigger value="login" className="flex-1">Sign In</TabsTrigger>
                      <TabsTrigger value="signup" className="flex-1">Sign Up</TabsTrigger>
                    </TabsList>
                    
                    <TabsContent value="login">
                      <form onSubmit={handleLogin} className="space-y-4">
                        <div className="space-y-2">
                          <Label htmlFor="login-email">Email</Label>
                          <div className="relative">
                            <Mail className="absolute left-3 top-3 h-4 w-4 text-muted-foreground" />
                            <Input
                              id="login-email"
                              type="email"
                              placeholder="Enter your email"
                              className="pl-10 glass"
                              value={loginData.email}
                              onChange={(e) => setLoginData(prev => ({ ...prev, email: e.target.value }))}
                              required
                            />
                          </div>
                        </div>
                        
                        <div className="space-y-2">
                          <Label htmlFor="login-password">Password</Label>
                          <div className="relative">
                            <Lock className="absolute left-3 top-3 h-4 w-4 text-muted-foreground" />
                            <Input
                              id="login-password"
                              type="password"
                              placeholder="Enter your password"
                              className="pl-10 glass"
                              value={loginData.password}
                              onChange={(e) => setLoginData(prev => ({ ...prev, password: e.target.value }))}
                              required
                            />
                          </div>
                        </div>
                        
                        <Button 
                          type="submit" 
                          variant="hero" 
                          className="w-full mt-6"
                          disabled={isLoading}
                        >
                          {isLoading ? "Signing in..." : "Sign In"}
                        </Button>
                        
                        <div className="text-center mt-4">
                          <Link to="/reset-password">
                            <Button variant="ghost" size="sm">
                              Forgot password?
                            </Button>
                          </Link>
                        </div>
                        
                        {/* Divider */}
                        <div className="relative my-6">
                          <div className="absolute inset-0 flex items-center">
                            <span className="w-full border-t border-border" />
                          </div>
                          <div className="relative flex justify-center text-xs uppercase">
                            <span className="bg-card px-2 text-muted-foreground">Or continue with</span>
                          </div>
                        </div>

                        {/* Google Sign-In Button */}
                        <Button
                          type="button"
                          variant="outline"
                          className="w-full glass"
                          onClick={handleGoogleSignIn}
                          disabled={isLoading}
                        >
                          <svg className="mr-2 h-4 w-4" viewBox="0 0 24 24">
                            <path
                              fill="currentColor"
                              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                            />
                            <path
                              fill="currentColor"
                              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                            />
                            <path
                              fill="currentColor"
                              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                            />
                            <path
                              fill="currentColor"
                              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                            />
                          </svg>
                          Continue with Google
                        </Button>
                      </form>
                    </TabsContent>
                    
                    <TabsContent value="signup">
                      <form onSubmit={handleSignup} className="space-y-4">
                        <div className="space-y-2">
                          <Label htmlFor="signup-name">Full Name</Label>
                          <div className="relative">
                            <User className="absolute left-3 top-3 h-4 w-4 text-muted-foreground" />
                            <Input
                              id="signup-name"
                              type="text"
                              placeholder="Enter your full name"
                              className="pl-10 glass"
                              value={signupData.name}
                              onChange={(e) => setSignupData(prev => ({ ...prev, name: e.target.value }))}
                              required
                            />
                          </div>
                        </div>
                        
                        <div className="space-y-2">
                          <Label htmlFor="signup-email">Email</Label>
                          <div className="relative">
                            <Mail className="absolute left-3 top-3 h-4 w-4 text-muted-foreground" />
                            <Input
                              id="signup-email"
                              type="email"
                              placeholder="Enter your email"
                              className="pl-10 glass"
                              value={signupData.email}
                              onChange={(e) => setSignupData(prev => ({ ...prev, email: e.target.value }))}
                              required
                            />
                          </div>
                        </div>
                        
                        <div className="space-y-2">
                          <Label htmlFor="signup-password">Password</Label>
                          <div className="relative">
                            <Lock className="absolute left-3 top-3 h-4 w-4 text-muted-foreground" />
                            <Input
                              id="signup-password"
                              type="password"
                              placeholder="Create a password"
                              className="pl-10 glass"
                              value={signupData.password}
                              onChange={(e) => setSignupData(prev => ({ ...prev, password: e.target.value }))}
                              required
                            />
                          </div>
                          
                          {/* Password Strength Indicators */}
                          {signupData.password && (
                            <div className="mt-2 p-3 border border-border rounded-lg bg-card/50 space-y-2">
                              <div className="flex items-center justify-between mb-2">
                                <p className="text-xs font-medium">Password Strength:</p>
                                <span className={`text-xs font-semibold ${getPasswordStrengthColor(validatePasswordStrength(signupData.password).score)}`}>
                                  {getPasswordStrengthLabel(validatePasswordStrength(signupData.password).score)}
                                </span>
                              </div>
                              <div className="w-full bg-gray-200 rounded-full h-1.5 mb-2">
                                <div
                                  className={`h-1.5 rounded-full transition-all ${
                                    validatePasswordStrength(signupData.password).score <= 1
                                      ? "bg-red-500"
                                      : validatePasswordStrength(signupData.password).score === 2
                                      ? "bg-yellow-500"
                                      : validatePasswordStrength(signupData.password).score === 3
                                      ? "bg-blue-500"
                                      : "bg-green-500"
                                  }`}
                                  style={{ width: `${(validatePasswordStrength(signupData.password).score / 4) * 100}%` }}
                                />
                              </div>
                              <div className="space-y-1">
                                <div className="flex items-center gap-2 text-xs">
                                  {signupData.password.length >= 8 ? (
                                    <Check className="w-3 h-3 text-green-500" />
                                  ) : (
                                    <X className="w-3 h-3 text-muted-foreground" />
                                  )}
                                  <span className={signupData.password.length >= 8 ? "text-green-500" : "text-muted-foreground"}>
                                    At least 8 characters
                                  </span>
                                </div>
                                <div className="flex items-center gap-2 text-xs">
                                  {/[A-Z]/.test(signupData.password) ? (
                                    <Check className="w-3 h-3 text-green-500" />
                                  ) : (
                                    <X className="w-3 h-3 text-muted-foreground" />
                                  )}
                                  <span className={/[A-Z]/.test(signupData.password) ? "text-green-500" : "text-muted-foreground"}>
                                    One uppercase letter
                                  </span>
                                </div>
                                <div className="flex items-center gap-2 text-xs">
                                  {/[0-9]/.test(signupData.password) ? (
                                    <Check className="w-3 h-3 text-green-500" />
                                  ) : (
                                    <X className="w-3 h-3 text-muted-foreground" />
                                  )}
                                  <span className={/[0-9]/.test(signupData.password) ? "text-green-500" : "text-muted-foreground"}>
                                    One number
                                  </span>
                                </div>
                              </div>
                            </div>
                          )}
                        </div>
                        
                        <div className="space-y-2">
                          <Label htmlFor="signup-confirm">Confirm Password</Label>
                          <div className="relative">
                            <Lock className="absolute left-3 top-3 h-4 w-4 text-muted-foreground" />
                            <Input
                              id="signup-confirm"
                              type="password"
                              placeholder="Confirm your password"
                              className="pl-10 glass"
                              value={signupData.confirmPassword}
                              onChange={(e) => setSignupData(prev => ({ ...prev, confirmPassword: e.target.value }))}
                              required
                            />
                          </div>
                        </div>
                        
                        <Button 
                          type="submit" 
                          variant="hero" 
                          className="w-full mt-6"
                          disabled={isLoading}
                        >
                          {isLoading ? "Creating account..." : "Create Account"}
                        </Button>
                        
                        {/* Divider */}
                        <div className="relative my-6">
                          <div className="absolute inset-0 flex items-center">
                            <span className="w-full border-t border-border" />
                          </div>
                          <div className="relative flex justify-center text-xs uppercase">
                            <span className="bg-card px-2 text-muted-foreground">Or continue with</span>
                          </div>
                        </div>

                        {/* Google Sign-In Button */}
                        <Button
                          type="button"
                          variant="outline"
                          className="w-full glass"
                          onClick={handleGoogleSignIn}
                          disabled={isLoading}
                        >
                          <svg className="mr-2 h-4 w-4" viewBox="0 0 24 24">
                            <path
                              fill="currentColor"
                              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                            />
                            <path
                              fill="currentColor"
                              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                            />
                            <path
                              fill="currentColor"
                              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                            />
                            <path
                              fill="currentColor"
                              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                            />
                          </svg>
                          Continue with Google
                        </Button>
                      </form>
                    </TabsContent>
                  </Tabs>
                  
                  <div className="mt-6 text-center text-xs text-muted-foreground">
                    By signing in, you agree to our{" "}
                    <a href="/legal" className="text-primary hover:underline">
                      Terms of Service
                    </a>{" "}
                    and{" "}
                    <a href="/legal" className="text-primary hover:underline">
                      Privacy Policy
                    </a>
                  </div>
                </CardContent>
              </Card>
            </motion.div>
          </div>
        </section>
      </main>
      <Footer />
    </div>
  );
};

export default Auth;